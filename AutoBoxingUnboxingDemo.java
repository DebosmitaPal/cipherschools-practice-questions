public class AutoBoxingUnboxingDemo {
    public static void main(String[] args) {
        Integer num1 = 25; 
        Integer num2 = 35;  
        int sum = num1 + num2;
        System.out.println("Sum of two Integer objects: " + sum);
        Double d1 = 12.5; 
        Double d2 = 7.5;  
        double result = d1 + d2;
        System.out.println("Sum of two Double objects: " + result);
    }
}

