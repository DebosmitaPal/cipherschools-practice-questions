import java.util.*;
public class operations {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        int b=sc.nextInt();
        int sum=a+b;
        int difference=a-b;
        int product=a*b;
        int divide=a/b;
        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(divide);
    }
}
